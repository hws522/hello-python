#input() : 사용자로 부터 콘솔로 입력을 받는 함수
#int() : 정수 자료형으로 변환
#float() : 문자열, 정수 등의 자료형을 실수형으로 변환
#max(), min() : 시퀀스 자료형에 포함되어 있는 원소중 최대값, 최솟값을 출력
#bin(), hex() : 10진수 -> 2진수 , 10진수 -> 16진수 변환
#int(0b2진수 , 2) 2진수 -> 10진수 , int(0x16진수, 16) -> 16진수 -> 10진수
#round() : 반올림 수행. round(숫자 , 2) : 소숫점 둘째자리까지 출력. round(숫자, -1) : 소수점 앞 첫자리에서 반올림.
#type() : 자료형의 종류 출력.
